package org.example.models.monitor.nextStopEta;

public class DeckEta {
    int area;
    String current_position;
    String next_stop;
    String load_percentage;

    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public String getCurrent_position() {
        return current_position;
    }

    public void setCurrent_position(String current_position) {
        this.current_position = current_position;
    }

    public String getNext_stop() {
        return next_stop;
    }

    public void setNext_stop(String next_stop) {
        this.next_stop = next_stop;
    }

    public String getLoad_percentage() {
        return load_percentage;
    }

    public void setLoad_percentage(String load_percentage) {
        this.load_percentage = load_percentage;
    }
}
